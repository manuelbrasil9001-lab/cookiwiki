// ---- CORE FUNCTIONS ----

function t(key) {
  const lang = STATE.language || 'es';
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.es;
  return dict[key] || TRANSLATIONS.es[key] || key;
}

// BUG FIX: rebuild lang menu with correct flags from LANG_META
function buildLangMenu() {
  const menu = document.getElementById('langMenu');
  if (!menu) return;
  menu.innerHTML = Object.entries(LANG_META).map(([code, meta]) =>
    `<button onclick="setLanguage('${code}')">${meta.flag} ${meta.label}</button>`
  ).join('');
}

function setLanguage(lang) {
  document.documentElement.lang = lang;
  STATE.language = lang;
  localStorage.setItem('cookiwiki_lang', lang);
  const meta = LANG_META[lang] || LANG_META.es;
  document.getElementById('currentFlag').textContent = meta.flag;
  document.getElementById('currentLang').textContent = meta.code;
  document.getElementById('langMenu').classList.remove('open');
  applyTranslations();
  if (typeof currentPage !== 'undefined' && currentPage === 'recipe' && STATE.currentRecipeId) {
    openRecipe(STATE.currentRecipeId);
  } else {
    renderCurrentPage();
  }
}

function applyTranslations() {
  // data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  // data-i18n-placeholder elements
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  // document title
  document.title = t('hero_eyebrow') + ' — CookiWiki';
}

function toggleLangMenu() {
  document.getElementById('langMenu').classList.toggle('open');
}

function detectLanguage() {
  const saved = localStorage.getItem('cookiwiki_lang');
  if (saved && TRANSLATIONS[saved]) return saved;
  const browser = (navigator.language || navigator.userLanguage || 'es').substring(0, 2).toLowerCase();
  return TRANSLATIONS[browser] ? browser : 'es';
}
